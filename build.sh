{
  echo "  <script>"
  for f in $JS_FILES; do
    cat "$f"
    echo ""
  done
  echo "  </script>"
} > /tmp/dlv_snippet.txt
replace_placeholder "$SRC/00-full-page.html" /tmp/dlv_snippet.txt DataLaVista.html
echo "  -> DataLaVista.html"

