PLACEHOLDER_OPEN="<!-- DATALAVISTA_SCRIPTS_PLACEHOLDER_OPEN -->"
PLACEHOLDER_CLOSE="<!-- DATALAVISTA_SCRIPTS_PLACEHOLDER_CLOSE -->"

# Collect JS source files in sort order (01-*.js through 15-*.js)
JS_FILES=$(ls "$SRC"/[0-9][0-9]-*.js | sort)

# ── 1. Build datalavista.js ──────────────────────────────────────────────────
